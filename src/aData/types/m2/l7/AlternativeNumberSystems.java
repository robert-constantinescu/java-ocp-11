package aData.types.m2.l7;

public class AlternativeNumberSystems {

    public static void main(String[] args) {
        System.out.println("\n** Alternative Number Systems ** \n");

        /**
         *
         * the underscores rules remain similar.
         * Rule of thumb, underscores should be used only inside the number
         */

        int baseTen = 100;      // base 10 literal
        int baseOctal = 0144;   // Octal literal starts with 0
        int baseHexa = 0x0064;  // hexadecimal
        int binary = 0b1100100; // binary literal
    }
}
