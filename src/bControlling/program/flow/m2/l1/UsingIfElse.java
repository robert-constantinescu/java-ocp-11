package bControlling.program.flow.m2.l1;

public class UsingIfElse {

    public static void main(String[] args) {
        int val1 = 10;
        int val2 = 4;

        // IF statement MUST have the CONDITION between PARENTHESIS
        if (val1 > val2) {// the expression MUST BE something the RESOLVE to TRUE or FALSE
            // TRUE statement
            System.out.println("Val1 is bigger");
        } else {
            //FALSE statement
            // the ELSE clause is OPTIONAL
            System.out.println("val1 is NOT bigger");
        }

    }
}
